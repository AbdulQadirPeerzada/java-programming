class datatypes
     {
       public static void main(String[]args)
       {
         int a=20;
         int b=25;
         System.out.println("a=20");
         System.out.println("b=25");

         double d=15.5;
         double c=10.8;
         System.out.println("c=15.5");
         System.out.println("d=10.8");

         char e='e';
         char f='g';
         System.out.println("e='e'");
         System.out.println("f='g'");


  }
}


